public class Operaters11 {
    public static void main(String[] args) {
        int A = 5;
        int B = 5;
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A/B);
        System.out.println(A%B);
        System.out.println(A*B);
    }
    
}
