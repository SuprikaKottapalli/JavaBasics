import java.util.*;
public class PrimeNum30 {
    public static void main(String[] args) {
       Scanner sc =  new Scanner(System.in);
       System.out.print("Enter the range: ");
       int a = sc.nextInt();
       int counter = 1;
       while (counter <= a ) {
       System.out.print(counter + " ");
       counter++;
        }
       }
    }