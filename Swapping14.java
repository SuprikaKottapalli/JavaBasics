public class Swapping14 {
    public static void main(String[] args) {
        
        int temp = 0;
        int a = 5;
        int b = 10;
        //Swapping
        temp = a;
        a = b;
        b = temp;
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("Temp="+temp);
    }
    
}