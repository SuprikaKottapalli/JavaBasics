import java.util.*;
public class EvenOdd16 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        if ( A % 2 == 0)
        {
        System.out.println("Even");
        }
        else
         {
            System.out.println("odd");
        }
         
    }
}
